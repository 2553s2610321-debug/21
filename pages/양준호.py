import random

# 1. 등급별 대학 데이터베이스 (예시 데이터)
# 실제 입결과는 다를 수 있으니 재미로 봐주세요!
university_db = {
    1: ["서울대학교", "KAIST", "연세대학교", "고려대학교", "포항공과대학교"],
    2: ["서강대학교", "성균관대학교", "한양대학교", "중앙대학교", "경희대학교"],
    3: ["서울시립대학교", "건국대학교", "동국대학교", "홍익대학교", "숙명여자대학교"],
    4: ["국민대학교", "숭실대학교", "세종대학교", "단국대학교", "광운대학교"],
    5: ["가천대학교", "경기대학교", "명지대학교", "인천대학교", "상명대학교"],
    6: ["가톨릭관동대학교", "강릉원주대학교", "공주대학교", "순천향대학교"],
    7: ["선문대학교", "백석대학교", "상지대학교", "세명대학교"],
    8: ["중부대학교", "동양대학교", "유원대학교"],
    9: ["어디든 원서를 넣으면 환영받는 귀한 인재입니다! (재도전도 멋진 선택)"]
}

def recommend_university():
    print("=" * 40)
    print("      🏫 내 성적 맞춤 대학 무작위 추천기 🏫      ")
    print("=" * 40)
    
    try:
        # 사용자로부터 평균 등급 입력 받기
        grade_input = float(input("자신의 평균 등급을 입력하세요 (1 ~ 9): "))
        
        # 소수점 입력 시 반올림하여 정수 등급으로 변환 (예: 2.3등급 -> 2등급)
        grade = round(grade_input)
        
        # 등급 범위 예외 처리
        if grade < 1 or grade > 9:
            print("❌ 올바른 등급(1~9)을 입력해주세요.")
            return

        # 해당 등급의 대학 리스트 가져오기
        available_unis = university_db[grade]
        
        # 무작위로 하나 선택
        chosen_uni = random.choice(available_unis)
        
        # 결과 출력
        print("\n" + "-" * 40)
        print(f"📊 입력하신 등급: {grade_input} (반올림 기준: {grade}등급)")
        print(f"✨ 당신의 운명이 이끄는 대학은... **[{chosen_uni}]** 입니다!")
        print("-" * 40)
        
    except ValueError:
        print("❌ 숫자만 입력해주세요! (예: 2.5 또는 4)")

# 프로그램 실행
if __name__ == "__main__":
    recommend_university()
